// 根据后缀和词根生成释义
function guessMeaning(word, root) {
  const lowerWord = word.toLowerCase();
  
  // 如果在完整释义库中找到，直接返回
  if (completeMeanings[lowerWord]) {
    return completeMeanings[lowerWord];
  }
  
  // 尝试匹配后缀
  let matchedSuffix = '';
  let suffixType = '';
  let suffixMeaning = '';
  
  for (const suffix of Object.keys(suffixPatterns)) {
    if (lowerWord.endsWith(suffix) && suffix.length > matchedSuffix.length) {
      matchedSuffix = suffix;
      suffixType = suffixPatterns[suffix].type;
      suffixMeaning = suffixPatterns[suffix].meaning;
    }
  }
  
  // 提取词根部分
  let rootPart = lowerWord;
  if (matchedSuffix) {
    rootPart = lowerWord.slice(0, -matchedSuffix.length);
  }
  
  // 构造释义
  let meaning = '';
  
  if (suffixType) {
    meaning = suffixType + ' ';
  }
  
  // 添加词根本身含义（如果有）
  if (root && root.length > 1 && root !== lowerWord) {
    // 词根可能已经有有意义的内容
    if (rootMeanings[root]) {
      meaning += rootMeanings[root] + '的';
    } else {
      meaning += word.charAt(0).toUpperCase() + word.slice(1, -matchedSuffix.length).toLowerCase();
    }
  } else if (suffixMeaning) {
    meaning += suffixMeaning;
  } else {
    meaning += word.charAt(0).toUpperCase() + word.slice(1);
  }
  
  if (!meaning.endsWith('的）') && !meaning.endsWith('...') && matchedSuffix && suffixMeaning) {
    if (!meaning.includes(suffixMeaning)) {
      meaning = meaning + '；' + suffixMeaning;
    }
  }
  
  return meaning || word;
}

